.MODEL	SMALL
.CODE
BEGIN:	JMP	SHORT MAIN
MESSAGE 	DB	'THIS IS CX 141$'
;	MAIN PROCEDURE:
;	--------------------------
MAIN	PROC	NEAR
	CALL	CLRS	;CLEAR SCREEN
	CALL	SETC	;SET CRUSOR
	CALL	PRINT	;PRINT THE ZIPCODE
	MOV	AX,4C00H	;EXIT TO DOS
	INT 21H
MAIN	ENDP
;	CLEAR	SCREEN:
;	--------------------------------
CLRS	PROC	NEAR
	MOV	AX, 06000H	;SCROLL FULL SCREEN
	MOV	BH,07		;ATTRIB : WH ON BLK
	MOV	CX, 0000		;UPPER LEFT LOC
	MOV	DX,184FH		;LOWER RIGHT LOC
	INT	10H
	RET
CLRS	ENDP
;	SET CURSOR TO 00,00:
;	--------------------------------------
SETC	PROC	NEAR
	MOV	AH,02H		;REQUEST SET CURSOR
	MOV	BH,00		;PAGE NUMBER 0
	MOV	DX,1233		;ROW 0, COLUMN 0
	INT	10H
	RET
SETC	ENDP
;	----------------------------------------
PRINT	PROC	NEAR
	MOV	AH,09H		;CODE FOR DISPLAY
	LEA	DX, MESSAGE	;ADDR OF TEXT
	INT	21H		;DOS CALL
PRINT	ENDP
	END	BEGIN
